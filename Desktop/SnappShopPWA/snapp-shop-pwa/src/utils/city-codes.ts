export const getCityTitle = (cityCode: string) => {
  const city = Object.keys(CITY_CODES).find(
    item => item.toLocaleLowerCase() === cityCode.toLocaleLowerCase()
  )
  const title = (city && Object(CITY_CODES)[city]?.title) || null
  return title
}

export const getCityCodeById = (cityId: string) => {
  const cityCode = Object.keys(CITY_CODES).find(
    item => Object(CITY_CODES)[item]?.id === cityId
  )
  return cityCode || null
}
